class node():
    def __init__(self):
        pass

    def setPort(self, port):
        self._port = port

    def getPort(self):
        return self._port

    def setStatus(self, status):
        self._status = status

    def getStatus(self):
        return self._status

    def setId(self, id):
        self._id = id

    def getId(self):
        return self._id

    def setIp(self, ip):
        self._ip = ip

    def getIp(self):
        return self._ip
